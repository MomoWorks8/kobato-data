export default function handler(req, res) {
  const now = new Date();
  const hour = now.getUTCHours() + 9; // JST補正
  const jstHour = (hour >= 24) ? hour - 24 : hour;

  let label = "day"; // fallback
  if (jstHour >= 5 && jstHour < 10) label = "morning";
  else if (jstHour >= 10 && jstHour < 17) label = "day";
  else if (jstHour >= 17 && jstHour < 22) label = "evening";
  else if (jstHour >= 22 && jstHour < 24) label = "night";
  else label = "midnight";

  const filtered = kuruppoData.filter(entry => entry.time === label);
  const message = filtered.length > 0
    ? filtered[Math.floor(Math.random() * filtered.length)].text
    : "ぽぽぽ〜 時空を超えて愛されてるよ🕊️";

  res.status(200).send(`🕊️ ${message}`);
}
